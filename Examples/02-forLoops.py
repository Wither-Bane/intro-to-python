##Intro

#'For loops' are one method of iteration, a mechanism that allows you to repeatedly run one or more lines of code in sequence until a condition is met.
#In the case of For Loops, it is a count-controlled iteration whereby the code inside the 'For' block is repeated for a predetermined number of times

#The format of a For Loop is:

"""
for variable in <expression>:
    <commands>
"""
#The loop executes '<commands>' for a fixed number of times, given by '<expression>'. The 'variable' may be used within '<commands>' to increase its capability, or it may be left out.

#For example:

for i in range(6):
    print("KFC loves Tango, and I love KFC")
    
